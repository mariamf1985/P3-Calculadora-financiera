
/*Creamos la clase "Calculadora" y agregamos los métodos de las operaciones que tendrán 2 argumentos*/

class Calculator {
    addition(num1, num2) {
        return num1 + num2;
    }
    subtraction(num1, num2) {
        return num1 - num2;
    }
    division(num1, num2) {
        return num1 / num2;
    }
    multiplication(num1, num2) {
        return num1 * num2;
    }
}